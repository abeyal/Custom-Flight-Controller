function [H]=computeJacobian(x,G)
H = zeros(3,4);
A1 = [x(1) x(4) x(3)*(-1);x(4)*(-1) x(1) x(2);x(3) x(2)*(-1) x(1)];
A1 = A1*2;
A2 = [x(2) x(3) x(4);x(3) x(2)*(-1) x(1);x(4) x(1)*(-1) x(2)*(-1)];
A2 = A2*2;
A3 = [x(3)*(-1) x(2) x(1)*(-1);x(2) x(3) x(4);x(1) x(4) x(3)*(-1)];
A3 = A3*2;
A4 = [x(4)*(-1) x(1) x(2);x(1)*(-1) x(4)*(-1) x(3);x(2) x(3) x(4)];
A4 = A4*2;
H = [A1*G, A2*G, A3*G, A4*G];